package Methods_In_Java;
public class Builtin_Function {
    public static void main(String[] args){
        System.out.println(Math.sqrt(100));//10
        System.out.println(Math.cbrt(100));//4.641588833612779
        System.out.println(Math.floor(-7.8));//-8.0
        System.out.println(Math.ceil(100.8));//101
        System.out.println(Math.max(10,7));//10
        System.out.println(Math.min(10,7));//7
        // Sawal --> 3 number ka max kese nikale
        System.out.println(Math.max(10,Math.max(5,6)));//10
        System.out.println(Math.pow(10,7));//1.0E7
    }
}
