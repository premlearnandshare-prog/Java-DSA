package String_Prem_Prectice;

public class Builtin_Methodes {
    public static void main(String[] args){
         String s = "Prem Kumar";
        System.out.println(s.indexOf('P'));
        System.out.println(s.toLowerCase());
        System.out.println(s.toUpperCase());
        System.out.println(s.contains("umar"));
        System.out.println(s.contains("iuwr"));
        System.out.println(s.startsWith("Pre"));
        System.out.println(s.startsWith("afjg"));
        System.out.println(s.endsWith("mar"));
        System.out.println(s.endsWith("Kum"));
        System.out.println();

    }
}
